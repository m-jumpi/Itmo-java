package lab2;

public class Calculator {
    public double sum(double x1, double x2) {
        return x1 + x2;
    }

    public long sum(long x1, long x2) {
        return x1 + x2;
    }

    public int sum(int x1, int x2) {
        return x1 + x2;
    }

    public double div(double x1, double x2) {
        return x1 / x2;
    }

    public long div(long x1, long x2) {
        return x1 / x2;
    }

    public int div(int x1, int x2) {
        return x1 / x2;
    }

    public double multp(double x1, double x2) {
        return x1 * x2;
    }

    public long multp(long x1, long x2) {
        return x1 * x2;
    }

    public int multp(int x1, int x2) {
        return x1 * x2;
    }

    public double sub(double x1, double x2) {
        return x1 - x2;
    }

    public long sub(long x1, long x2) {
        return x1 - x2;
    }

    public int sub(int x1, int x2) {
        return x1 - x2;
    }
}
